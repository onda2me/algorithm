package com.onda2me.algorithm.begin;
/*
	@문제
	a, b 두 정수의 a+b, a-b, a*b, a/b, a%b 출력하기

	@ 입력
	10
	3

	@ 출력
	13
	7
	30
	3
	1
*/

public class Begin04Oper04 {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);		
		System.out.println(a%b);

	}	
}