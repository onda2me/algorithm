package com.onda2me.algorithm.begin;

/*
	@문제
	a, b 두 정수의 합과 차 출력하기

	@ 입력
	5 3
	2 7

	@ 출력
	8, 2
	9, 5

*/

public class Begin04Oper03 {

	public static void main(String[] args) {

		int a = 5;
		int b = 3;

		System.out.println((a+b) + ", "+ (a-b));

		a = 7;
		b = 2;

		System.out.println((a+b) + ", "+ (a-b));		

	}	
}